ALTER TABLE candidate_attempts
    ADD COLUMN result_webhook_url VARCHAR(1000);

CREATE TABLE result_deliveries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    candidate_attempt_id VARCHAR(80) NOT NULL,
    webhook_url VARCHAR(1000) NOT NULL,
    status VARCHAR(40) NOT NULL,
    attempt_count INTEGER NOT NULL,
    next_attempt_at TIMESTAMP WITH TIME ZONE,
    last_attempt_at TIMESTAMP WITH TIME ZONE,
    sent_at TIMESTAMP WITH TIME ZONE,
    last_error VARCHAR(1200),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_result_deliveries_candidate_attempt
        FOREIGN KEY (candidate_attempt_id)
        REFERENCES candidate_attempts (id)
        ON DELETE CASCADE,
    CONSTRAINT uk_result_delivery_attempt
        UNIQUE (candidate_attempt_id)
);

CREATE INDEX idx_result_deliveries_status_next_attempt
    ON result_deliveries (status, next_attempt_at);
